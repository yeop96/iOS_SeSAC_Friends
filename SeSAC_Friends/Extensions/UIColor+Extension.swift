import UIKit

extension UIColor {
    /// Black&White ColorSet
    class var white: UIColor { return UIColor(named: "white")! }
    class var black: UIColor { return UIColor(named: "black")! }
    /// Brand Color ColorSet
    class var green: UIColor { return UIColor(named: "green")! }
    class var whiteGreen: UIColor { return UIColor(named: "whitegreen")! }
    class var yellowGreen: UIColor { return UIColor(named: "yellowgreen")! }
    /// Grayscale ColorSet
    class var gray7: UIColor { return UIColor(named: "gray7")! }
    class var gray6: UIColor { return UIColor(named: "gray6")! }
    class var gray5: UIColor { return UIColor(named: "gray5")! }
    class var gray4: UIColor { return UIColor(named: "gray4")! }
    class var gray3: UIColor { return UIColor(named: "gray3")! }
    class var gray2: UIColor { return UIColor(named: "gray2")! }
    class var gray1: UIColor { return UIColor(named: "gray1")! }
    /// System Color ColorSet
    class var success: UIColor { return UIColor(named: "success")! }
    class var error: UIColor { return UIColor(named: "error")! }
    class var focus: UIColor { return UIColor(named: "focus")! }
}
